# 1-12-2 for loop
# 精典练习(2)：从 38 加到 113

numSum = 0 # 将 0 赋值给变量 numSum (numSum的初始和为 0 )

for i in range(76): # 循环76次，但是 i 的值是从 0 到 75
    
    numSum = numSum + (i + 38) # 因此从第一次循环开始，累加的值需加上一个 38 ，否则就成了从 0 加到 75。

print(numSum)


